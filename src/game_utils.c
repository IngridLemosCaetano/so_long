#include "../inc/so_long.h"

void	free_map(t_map *m)
{
	int	i;

	if (!m->grid)
		return ;
	i = 0;
	while (m->grid[i])
	{
		free(m->grid[i]);
		i++;
	}
	free(m->grid);
	m->grid = NULL;
}
